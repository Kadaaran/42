#include "libft.h"

char	*ft_strncpy(char *dst, char *src, size_t n)
{
	size_t c;

	c = 0;
	while (c < n && src[c] != '\0')
	{
		dst[c] = src[c];
		c++;
	}
	while (c < n)
	{
		dst[c] = '\0';
		c++;
	}
	return (dst);
}
